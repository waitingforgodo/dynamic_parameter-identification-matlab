function [q, qd, q2d] = fourier_series_traj(t, q0, A, B, w, N)
% -----------------------------------------------------------------------
% 矢量形式截断傅里叶级数轨迹生成函数
% 用于动力学辨识的激励轨迹（位置、速度、加速度）
% 优化过程中会被高频调用，因此采用向量化运算，无循环，速度极快
%
% 输入：
%   t    - 时间点向量 (s)
%   q0   - 初始关节位置 (n_dof x 1)
%   A    - 正弦项系数矩阵 (n_dof x N)
%   B    - 余弦项系数矩阵 (n_dof x N)
%   w    - 基频 (rad/s)
%   N    - 谐波次数
%
% 输出：
%   q    - 位置轨迹 (n_dof x n_t)
%   qd   - 速度轨迹 (n_dof x n_t)
%   q2d  - 加速度轨迹 (n_dof x n_t)
% -----------------------------------------------------------------------

% 把时间 t  reshape 为行向量，保证广播运算维度正确
t = reshape(t, 1, []);

% 获取时间点的数量
n_t = numel(t);

% 如果谐波次数 N=0，轨迹为静止：位置=q0，速度=0，加速度=0
if N == 0
    q = q0 * ones(1, n_t);
    qd = zeros(size(q));
    q2d = zeros(size(q));
    return;
end

% 生成谐波阶次向量 [1,2,...,N]'
harmonics = (1:N)';

% 计算 w*k*t，维度 (N x n_t)，用于所有谐波的三角函数计算
wt = w * harmonics * t;

% 预计算所有谐波的 sin(wt) 和 cos(wt)
sin_wt = sin(wt);
cos_wt = cos(wt);

% 1/(w*k)：位置积分系数
inv_wk = 1 ./ (w * harmonics);

% w*k：速度/加速度系数
wk = w * harmonics;

% -----------------------------------------------------------------------
% 位置：q = q0 + sum( (A_ik/(w*k))*sin(wkt) - (B_ik/(w*k))*cos(wkt) )
% 向量化矩阵运算，无循环
% -----------------------------------------------------------------------
q = q0 * ones(1, n_t) + (A .* inv_wk') * sin_wt - (B .* inv_wk') * cos_wt;

% -----------------------------------------------------------------------
% 速度：qd = sum( A_ik*cos(wkt) + B_ik*sin(wkt) )
% 对位置求导得到
% -----------------------------------------------------------------------
qd = A * cos_wt + B * sin_wt;

% -----------------------------------------------------------------------
% 加速度：q2d = sum( -A_ik*wkt*sin(wkt) + B_ik*wkt*cos(wkt) )
% 对速度求导得到
% -----------------------------------------------------------------------
q2d = -(A .* wk') * sin_wt + (B .* wk') * cos_wt;

end