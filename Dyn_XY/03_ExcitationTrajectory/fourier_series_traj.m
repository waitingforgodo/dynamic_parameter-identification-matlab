function [q,qd,q2d] = fourier_series_traj(t,q0,A,B,w,N)
% -----------------------------------------------------------------------
% 该函数用于评估（计算）截断傅里叶级数多项式。
% 输入:
%   t - 需要评估多项式的时间点
%   q0 - 初始偏移量，应为列向量 (nx1)
%   A - 正弦项系数
%   B - 余弦项系数
%   w - 基频
%   N - 谐波数量
% 输出:
%   q - 位置，即多项式的值
%   qd - 速度，即多项式的一阶导数
%   q2d - 加速度，即多项式的二阶导数
% -----------------------------------------------------------------------

%% 向量化版
% 【预处理】将时间向量 t 强制重塑为 1 行 n_t 列的行向量
% 这样做是为了保证后续矩阵乘法的维度兼容性，无论外部传入的是行向量还是列向量
t = reshape(t, 1, []);
n_t = numel(t); % 获取需要计算的时间点总数

% 【边界条件处理】如果谐波次数 N 为 0，说明不需要叠加任何波动
if N == 0
    q = q0 * ones(1, n_t);   % 位置恒等于初始偏移量 q0
    qd = zeros(size(q));     % 速度为 0
    q2d = zeros(size(q));    % 加速度为 0
    return;                  % 直接返回结果，避免后续无效计算
end

% 【构建谐波矩阵】
harmonics = (1:N)';          % 生成列向量 [1; 2; ...; N]，代表各个谐波的阶数 k
wt = w * harmonics * t;      % 利用外积计算所有时间点、所有阶数的相位 (w*k*t)，得到 N x n_t 的矩阵
sin_wt = sin(wt);            % 计算所有相位的正弦值矩阵
cos_wt = cos(wt);            % 计算所有相位的余弦值矩阵

% 【准备频率系数】
inv_wk = 1 ./ (w * harmonics); % 位置积分产生的系数：1/(w*k)，是一个 N x 1 的列向量
wk = w * harmonics;            % 加速度求导产生的系数：w*k，也是一个 N x 1 的列向量

% 【计算位置 q】
% 公式：q = q0 + sum( A_k/(w*k)*sin(w*k*t) - B_k/(w*k)*cos(w*k*t) )
% 这里利用矩阵乘法一次性完成对所有谐波和时间点的求和：
% (A .* inv_wk') 将系数 A 按列匹配上 1/(w*k)，再与 sin_wt 矩阵相乘实现求和
q = q0 * ones(1, n_t) + (A .* inv_wk') * sin_wt - (B .* inv_wk') * cos_wt;

% 【计算速度 qd】（位置的一阶导数）
% 公式：qd = sum( A_k*cos(w*k*t) + B_k*sin(w*k*t) )
% 同样利用矩阵乘法，A 乘以余弦矩阵，B 乘以正弦矩阵
qd = A * cos_wt + B * sin_wt;

% 【计算加速度 q2d】（速度的二阶导数）
% 公式：q2d = sum( -A_k*w*k*sin(w*k*t) + B_k*w*k*cos(w*k*t) )
% 这里将系数 wk (即 w*k) 广播到 A 和 B 上，再进行矩阵乘法
q2d = -(A .* wk') * sin_wt + (B .* wk') * cos_wt;


%% 循环版
% % 初始化位置为初始偏移量 q0
% q = q0;
% 
% % 初始化速度和加速度为零向量（维度与 q0 保持一致）
% qd = zeros(size(q0));
% q2d = zeros(size(q0));
% 
% % 循环叠加 N 个谐波的贡献
% for k = 1:N
%    % 【位置 q】：叠加 sin 和 cos 项。注意这里除以了 (w*k)，这是积分产生的系数
%    q = q + A(:,k)/(w*k).*sin(w*k*t) - B(:,k)/(w*k).*cos(w*k*t);
% 
%    % 【速度 qd】：位置对时间求一阶导数。sin求导变cos，cos求导变-sin（负负得正）
%    qd = qd + A(:,k).*cos(w*k*t) + B(:,k)*sin(w*k*t);
% 
%    % 【加速度 q2d】：速度对时间求二阶导数。多出了频率系数 (w*k)
%    q2d = q2d - A(:,k)*w*k.*sin(w*k*t) + B(:,k)*w*k*cos(w*k*t);




end
