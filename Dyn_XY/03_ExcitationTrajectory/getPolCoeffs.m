function out = getPolCoeffs(T,a,b,wf,N,q0)
% -----------------------------------------------------------------------
% 该函数根据有限傅里叶级数的系数 a 和 b，以及机器人的初始位置，计算出五阶多项式的系数。
% 输入:
%   T - 运动周期
%   a - 有限傅里叶级数中的正弦系数 (sin coeffs)
%   b - 有限傅里叶级数中的余弦系数 (cos coeffs)
%   wf - 基频
%   N - 谐波数量
%   q0 - 机器人的初始位置
% 输出:
%   out - 五阶多项式的系数矩阵
%         这些系数能保证轨迹在初始时刻和终止时刻的位置、速度和
%         加速度约束条件得到满足（即实现平滑启停）。
% -----------------------------------------------------------------------

n = size(q0, 1);

% 【核心优化】声明持久化（Persistent）变量
% 这些变量的值在函数调用结束后不会销毁，而是保留在内存中供下一次调用使用
persistent Ac_cache T_cache n_cache

% 【智能判断与缓存构建】
% 只有在第一次运行（缓存为空），或者外部传入的时间T、关节数n发生变化时，
% 才重新构建庞大的线性方程组矩阵 Ac。否则直接使用内存中的缓存，极大提升效率。
if isempty(Ac_cache) || isempty(T_cache) || isempty(n_cache) || T_cache ~= T || n_cache ~= n
    I = eye(n);    % 生成 n x n 的单位矩阵
    O = zeros(n);  % 生成 n x n 的零矩阵
    
    % 构建 6n x 6n 的线性方程组系数矩阵 Ac
    % 每一行分别对应 t=0 和 t=T 时刻的位置、速度、加速度约束条件
    Ac_cache = [I,  O,  O,  O,  O,  O; ...                % t=0 位置约束
                O,  I,  O,  O,  O,  O; ...                % t=0 速度约束
                O,  O,  2*I, O,  O,  O; ...               % t=0 加速度约束 (求导产生系数2)
                I,  T*I, T^2*I, T^3*I, T^4*I, T^5*I; ...  % t=T 位置约束
                O,  I,  2*T*I, 3*T^2*I, 4*T^3*I, 5*T^4*I; ... % t=T 速度约束
                O,  O,  2*I, 6*T*I, 12*T^2*I, 20*T^3*I];  % t=T 加速度约束 (求导产生系数)
    
    % 更新缓存的状态记录，以便下次判断是否需要重建矩阵
    T_cache = T;
    n_cache = n;
end

% 【计算傅里叶级数部分在 t=0 时刻产生的位置、速度和加速度】
qh0 = -sum(b ./ ((1:N) * wf), 2);      % 位置偏移 (由 cos 项积分产生)
qdh0 = sum(a, 2);                      % 速度偏移 (sin求导变cos)
q2dh0 = sum(b .* ((1:N) * wf), 2);     % 加速度偏移 (cos求导变-sin，负负得正)

% 【调用向量化函数，计算傅里叶级数部分在 t=T 时刻产生的状态】
[qhT, qdhT, q2dhT] = fourier_series_traj(T, zeros(n, 1), a, b, wf, N);

% 【构建线性方程组的右端项 (RHS)】
% 物理意义：多项式需要补偿的值 = 目标边界条件 - 傅里叶级数在该边界已产生的值
rhs = [q0 - qh0;   % t=0 时，多项式需要补偿的位置差值
       -qdh0;      % t=0 时，期望速度为0，需补偿的速度差值
       -q2dh0;     % t=0 时，期望加速度为0，需补偿的加速度差值
       q0 - qhT;   % t=T 时，多项式需要补偿的位置差值
       -qdhT;      % t=T 时，期望速度为0，需补偿的速度差值
       -q2dhT];    % t=T 时，期望加速度为0，需补偿的加速度差值

% 【求解线性方程组并输出结果】
c = Ac_cache \ rhs;            % 利用左除高效求解多项式的各项系数 c
out = reshape(c, [n, 6]);      % 将结果重塑为 [关节数 x 6个多项式系数] 的矩阵格式
end